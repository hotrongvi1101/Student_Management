    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        this.txtMSSV.setText("");
        this.txtDate.setText("");
        this.txtName.setText("");
        this.txtSDT.setText("");
        this.cbMale.setSelected(false);
        this.cbFemale.setSelected(false);
        this.cbCourses.setSelectedIndex(0);
        Switch(false, true);
        this.start = 1;
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String id = this.txtMSSV.getText();
        String date = this.txtDate.getText();
        String name = this.txtName.getText();
        String sdt = this.txtSDT.getText();
        String sex;
        if(this.cbMale.isSelected()== true){
            sex = this.cbMale.getText();
        }
        else{
            sex = this.cbFemale.getText();
        }
        String courses = (String) this.cbCourses.getSelectedItem();
        if(start == 1){
        list.add(new Student(id,name,date,sdt,sex,courses));
        }
        if(start == 0){
        list.set(n, new Student(id,name,date,sdt,sex,courses));
        }
        View();
        ViewTable();
        Switch(true, false);
        
    }//GEN-LAST:event_btnSaveActionPerformed

