    public void View(){
        sv = list.get(n);
        this.txtMSSV.setText(sv.getID());
        this.txtName.setText(sv.getFullName());
        this.txtDate.setText(sv.getDateofBirth());
        this.txtSDT.setText(sv.getPhoneNo());
        this.cbMale.setSelected("Nam".equals(sv.getSex()));
        this.cbFemale.setSelected("Nữ".equals(sv.getSex()));
        for(int i=0; i< 4; i++){
           if(sv.getKhoa() == cbCourses.getItemAt(i)){
            cbCourses.setSelectedIndex(i);
        } 
        }
    }
    public void ViewTable(){
        DefaultTableModel model = (DefaultTableModel) this.tbStudent.getModel();
        model.setNumRows(0);
        for(Student sv:list){
     
                model.addRow(new Object[]{sv.getID(),sv.getFullName(),sv.getKhoa(),sv.getDateofBirth(),sv.getSex(),sv.getPhoneNo()});
 
        }
    }

